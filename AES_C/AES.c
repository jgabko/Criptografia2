static void subbyte(uint8_t state[4][4]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            state[i][j] = s_box[state[i][j]];
        }
    }
}

static void subbyte_inv(uint8_t state[4][4]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            state[i][j] = s_box_inv[state[i][j]];
        }
    }
}

static void switchrow(uint8_t state[4][4]) {
    uint8_t tmp;
 
    /* Row 1: left shift by 1 */
    tmp          = state[1][0];
    state[1][0]  = state[1][1];
    state[1][1]  = state[1][2];
    state[1][2]  = state[1][3];
    state[1][3]  = tmp;
 
    /* Row 2: left shift by 2  (swap pairs) */
    tmp          = state[2][0]; state[2][0] = state[2][2]; state[2][2] = tmp;
    tmp          = state[2][1]; state[2][1] = state[2][3]; state[2][3] = tmp;
 
    /* Row 3: left shift by 3  (≡ right shift by 1) */
    tmp          = state[3][3];
    state[3][3]  = state[3][2];
    state[3][2]  = state[3][1];
    state[3][1]  = state[3][0];
    state[3][0]  = tmp;
}

static void switchrow_inv(uint8_t state[4][4]) {
    uint8_t tmp;
 
    /* Row 1: right shift by 1 */
    tmp          = state[1][3];
    state[1][3]  = state[1][2];
    state[1][2]  = state[1][1];
    state[1][1]  = state[1][0];
    state[1][0]  = tmp;
 
    /* Row 2: right shift by 2  (same as left shift by 2) */
    tmp          = state[2][0]; state[2][0] = state[2][2]; state[2][2] = tmp;
    tmp          = state[2][1]; state[2][1] = state[2][3]; state[2][3] = tmp;
 
    /* Row 3: right shift by 3  (≡ left shift by 1) */
    tmp          = state[3][0];
    state[3][0]  = state[3][1];
    state[3][1]  = state[3][2];
    state[3][2]  = state[3][3];
    state[3][3]  = tmp;
}

static void addroundkey(uint8_t state[4][4], uint8_t key[16]) {
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            state[j][i] ^= key[j + (i * 4)];
        }
    }
}

void keyexpansion(uint8_t key[32], uint8_t round_keys[15][16]) {
    memcpy(round_keys[0], key, 16);
    memcpy(round_keys[1], key + 16, 16);
    
    for (int round = 2; round <= 14; round++) {
        ExpansionOperation(round_keys[round - 1], round_keys[round], round);
    }
}

static void ExpansionOperation(uint8_t key_prev[16], uint8_t next_key[16], int round) {
    uint8_t last_word[4];
    uint8_t w_x[4];
    uint8_t w_y[4];
    uint8_t w_z[4];
    uint8_t w_w[4];
    
    for(int i = 0; i < 4; i++) {
       last_word[i] = key_prev[12 + i];
    }

    for(int i = 0; i < 4; i++) {
        w_x[i] = key_prev[i] ^ last_word[i];
    }

    for(int i = 0; i < 4; i++) {
        w_y[i] = w_x[i] ^ key_prev[4 + i];
    }
    for(int i = 0; i < 4; i++) {
        w_z[i] = w_y[i] ^ key_prev[8 + i];
    }
    for(int i = 0; i < 4; i++) {
        w_w[i] = w_z[i] ^ key_prev[12 + i];
    }

    // Removida a linha com erro (uint8_t new_word[16]={...}) pois não era usada.

    memcpy(next_key + 0,  w_x, 4);
    memcpy(next_key + 4,  w_y, 4);
    memcpy(next_key + 8,  w_z, 4);
    memcpy(next_key + 12, w_w, 4);
}

static void rotword(uint8_t w[4]) {
    uint8_t t = w[0];
    w[0] = w[1];
    w[1] = w[2];
    w[2] = w[3];
    w[3] = t;
}

static void s_word(uint8_t w[4]) {
    for (int i = 0; i < 4; i++) {
        w[i] = s_box[w[i]]; 
    }
}

static void round_const(uint8_t w[4], int round) {
    w[0] ^= RCON[round];
}

static void function_g(uint8_t w[4], int round) {
    rotword(w);
    s_word(w);
    round_const(w, round);
}

static void mixColumns(uint8_t state[4][4], const uint8_t mix[4][4]) {
    uint8_t temp[4][4];

    for (int col = 0; col < 4; col++) {
        for (int row = 0; row < 4; row++) {
            temp[row][col] = 0;
            for (int k = 0; k < 4; k++)
                temp[row][col] ^= gmul(mix[row][k], state[k][col]);
        }
    }

    memcpy(state, temp, 16);
}

static uint8_t gmul(uint8_t a, uint8_t b) {
    uint8_t result = 0;
    uint8_t hi_bit;

    for (int i = 0; i < 8; i++) {
        if (b & 1)
            result ^= a;

        hi_bit = a & 0x80;
        a <<= 1;

        if (hi_bit)
            a ^= 0x1B;

        b >>= 1;
    }
    return result;
}

static void bytes_to_state(const uint8_t in[16], uint8_t state[4][4]) {
    for (int col = 0; col < 4; col++) {
        for (int row = 0; row < 4; row++) {
            state[row][col] = in[row + (col * 4)];
        }
    }
}

static void state_to_bytes(const uint8_t state[4][4], uint8_t out[16]) {
    for (int col = 0; col < 4; col++)
        for (int row = 0; row < 4; row++)
            out[col * 4 + row] = state[row][col];
}

void encrypt_block(uint8_t block[16], uint8_t round_keys[15][16]) {
    uint8_t state[4][4];

    bytes_to_state(block, state);

    addroundkey(state, round_keys[0]);

    for (int r = 1; r <= 13; r++) {
        subbyte(state);                        /* SubBytes */
        switchrow(state);                      /* ShiftRows */
        mixColumns(state, mix);                /* MixColumns */
        addroundkey(state, round_keys[r]);     /* AddRoundKey */
    }

    /* Rodada final 14 (sem MixColumns) */
    subbyte(state);                            /* SubBytes */
    switchrow(state);                          /* ShiftRows */
    addroundkey(state, round_keys[14]);        /* AddRoundKey */

    state_to_bytes(state, block);
}

void dencrypt_block(uint8_t block[16], uint8_t round_keys[15][16]) {
    uint8_t state[4][4];

    bytes_to_state(block, state);

    addroundkey(state, round_keys[14]);

    for (int r = 13; r <= 1; r--) {                        
        switchrow_inv(state);  
        subbyte_inv(state);                                  
        addroundkey(state, round_keys[r]);     
        mixColumns(state, mix_inv);  
    }

    /* Rodada final 14 (sem MixColumns) */
    switchrow_inv(state);  
    subbyte_inv(state);                                  
    addroundkey(state, round_keys[0]);     
         
    state_to_bytes(state, block);
}